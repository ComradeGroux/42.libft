#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;
	size_t	temp;

	i = ft_strlen(dst);
	j = 0;
	temp = ft_strlen(src) + ft_strlen(dst);
	while (src[j] != '\0' && i < dstsize - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	return (temp);
}
